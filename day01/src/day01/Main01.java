package day01;

/*
 * 프로그램 소스의 최소 단위(=class)를 명시하는 블록
 * -> public class 크래스이름
 * 클래스 이름은 소스파일의 이름과 동일해야 하며, 영어/숫자/언더바(_)만
 * 사용 가능하다.
 * 첫 글자는 반드시 영어로만 구성되어야 한다. 
 */
public class Main01 {// 프로그램의 시작점을 의미하는 블록 -> main 메서드
	public static void main(String[] args) {
		// 콘솔에 문장을 출력하기 위한 명령어
		// 문장을 표현하는 방법 -> 쌍따옴표로 묶는다. -> 문자열
		System.out.println("Hello World!!!");
		System.out.println("kjh");
	}
}






